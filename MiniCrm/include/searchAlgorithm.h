#ifndef SEARCHALGORITHM_H
#define SEARCHALGORITHM_H

#include "client.h"

/**
 * @brief Линейный поиск клиентов по ФИО.
 *
 * Последовательно проверяет всех клиентов в файле clients.bin.
 * Выводит все совпадения с указанным ФИО.
 *
 * @note Не требует предварительной сортировки данных
 */
void searchLinearByName();

/**
 * @brief Бинарный поиск клиентов по товару.
 *
 * Требует предварительной сортировки данных по названию товара.
 * Выводит всех клиентов с указанным товаром.
 *
 * @warning В текущей реализации сортирует данные по товару перед поиском
 */
void searchBinaryByProduct();

/**
 * @brief Поиск клиентов по товару и дате.
 *
 * Находит клиентов, купивших указанный товар после заданной даты.
 * Результаты сортируются по ФИО.
 */
void searchByProductAndDate();

/**
 * @brief Линейный поиск клиентов по товару.
 *
 * Выводит всех клиентов, связанных с указанным товаром.
 */
void searchLinearByProduct();

#endif